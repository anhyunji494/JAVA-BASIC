package 개발자괴롭히기;

public class enemy {
	
	String name;
	int hp;
	int intell ;
	int fp;
	int mp;
	int ment;
	
	public enemy(String name, int hp, int intell, int fp, int mp, int ment) {
		super();
		this.name = name;
		this.hp = hp;
		this.intell = intell;
		this.fp = fp;
		this.mp = mp;
		this.ment = ment;
	}

}
